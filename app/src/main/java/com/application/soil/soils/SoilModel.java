class SoilModel {

    String name;

    String potash;

    String phos;
    String alkali;
    String nitro;
    String iron;
    String lime;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPotash() {
        return potash;
    }

    public void setPotash(String potash) {
        this.potash = potash;
    }

    public String getPhos() {
        return phos;
    }

    public void setPhos(String phos) {
        this.phos = phos;
    }

    public String getAlkali() {
        return alkali;
    }

    public void setAlkali(String alkali) {
        this.alkali = alkali;
    }

    public String getNitro() {
        return nitro;
    }

    public void setNitro(String nitro) {
        this.nitro = nitro;
    }

    public String getIron() {
        return iron;
    }

    public void setIron(String iron) {
        this.iron = iron;
    }

    public String getLime() {
        return lime;
    }

    public void setLime(String lime) {
        this.lime = lime;
    }
}
